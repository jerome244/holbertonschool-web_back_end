#!/usr/bin/env python3

import math

def floor(n: float) -> int:
    """
    This function takes a float n as argument and returns the floor of the float.

    Args:
        n (float): The float value for which we need to compute the floor.

    Returns:
        int: The floor of the input float n.

    Example:
        >>> floor(3.14)
        3
    """
    return math.floor(n)
