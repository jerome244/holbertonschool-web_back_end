#!/usr/bin/env python3

def to_str(n: float) -> str:
    """
    Convert a float to its string representation.

    Args:
        n (float): The float value to be converted into a string.

    Returns:
        str: The string representation of the input float n.

    Example:
        >>> to_str(3.14)
        '3.14'
    """
    return str(n)
